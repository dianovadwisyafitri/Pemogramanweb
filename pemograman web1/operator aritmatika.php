<?php
// Deklarasi variabel
$a = 10;
$b = 5;

// Penjumlahan
$penjumlahan = $a + $b;
echo "Hasil penjumlahan $a + $b = $penjumlahan<br>";

// Pengurangan
$pengurangan = $a - $b;
echo "Hasil pengurangan $a - $b = $pengurangan<br>";

// Perkalian
$perkalian = $a * $b;
echo "Hasil perkalian $a * $b = $perkalian<br>";

// Pembagian
$pembagian = $a / $b;
echo "Hasil pembagian $a / $b = $pembagian<br>";

// Sisa bagi (modulus)
$modulus = $a % $b;
echo "Sisa bagi (modulus) $a % $b = $modulus<br>";

// Eksponensial (pangkat)
$eksponensial = $a ** $b;
echo "Hasil eksponensial $a ** $b = $eksponensial<br>";
?>
